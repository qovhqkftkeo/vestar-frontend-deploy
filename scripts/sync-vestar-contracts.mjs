import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

// `/home/sp/VESTAr/contracts/abi`에 있는 handoff ABI/address 파일을
// 프론트 전용 TS 모듈로 옮겨서 import ergonomics를 맞추는 스크립트다.
//
// 생성 결과:
// - `src/contracts/vestar/generated/*Abi.ts`
//   각 ABI json array를 `as const satisfies Abi` 형태로 export
// - `src/contracts/vestar/generated/addresses.ts`
//   현재 사용 가능한 최신 VESTAr 배포 주소 스냅샷을 프론트에서 바로 쓰는 상수로 export
// - `src/contracts/vestar/generated/index.ts`
//   generated 파일들의 barrel export

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const frontendRoot = path.resolve(__dirname, '..')
const abiSourceDir = path.resolve(frontendRoot, '../contracts/abi')
const outputDir = path.resolve(frontendRoot, 'src/contracts/vestar/generated')

// source ABI 파일명과 생성될 프론트 모듈명을 1:1로 매핑한다.
// 새 컨트랙트를 handoff 받을 때는 여기만 추가하면 된다.
const ABI_FILES = [
  {
    source: 'VESTArElection.json',
    output: 'vestarElectionAbi.ts',
    exportName: 'vestarElectionAbi',
  },
  {
    source: 'VESTArElectionFactory.json',
    output: 'vestarElectionFactoryAbi.ts',
    exportName: 'vestarElectionFactoryAbi',
  },
  {
    source: 'VESTArOrganizerRegistry.json',
    output: 'vestarOrganizerRegistryAbi.ts',
    exportName: 'vestarOrganizerRegistryAbi',
  },
  {
    source: 'VESTArKarmaRegistry.json',
    output: 'vestarKarmaRegistryAbi.ts',
    exportName: 'vestarKarmaRegistryAbi',
  },
  {
    source: 'MockUSDT.json',
    output: 'mockUsdtAbi.ts',
    exportName: 'mockUsdtAbi',
  },
]

const GENERATED_FILE_BANNER =
  '// This file is auto-generated by scripts/sync-vestar-contracts.mjs.\n'

function formatTsModule(body) {
  return `${GENERATED_FILE_BANNER}\n${body.trimEnd()}\n`
}

async function generateAbiModules() {
  for (const file of ABI_FILES) {
    const sourcePath = path.join(abiSourceDir, file.source)
    const targetPath = path.join(outputDir, file.output)
    const abiJson = JSON.parse(await readFile(sourcePath, 'utf8'))
    const moduleSource = formatTsModule(
      [
        'import type { Abi } from "viem";',
        '',
        `export const ${file.exportName} = ${JSON.stringify(abiJson, null, 2)} as const satisfies Abi;`,
      ].join('\n'),
    )

    await writeFile(targetPath, moduleSource)
  }
}

async function resolveAddressSourceFile() {
  const candidates = ['status-hoodi.addresses.json', 'status-sepolia.addresses.json']

  for (const filename of candidates) {
    const sourcePath = path.join(abiSourceDir, filename)

    try {
      await readFile(sourcePath, 'utf8')
      return { filename, sourcePath }
    } catch (error) {
      if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
        continue
      }

      throw error
    }
  }

  throw new Error(
    `Could not find a deployment address file in ${abiSourceDir}. Expected one of: ${candidates.join(', ')}`,
  )
}

async function generateAddressesModule() {
  // 배포 주소 파일은 컨트랙트 팀의 naming을 유지하면서도,
  // 프론트에서는 camelCase key로 바로 쓰기 쉽게 한 번 더 정리한다.
  const { filename, sourcePath } = await resolveAddressSourceFile()
  const targetPath = path.join(outputDir, 'addresses.ts')
  const rawAddresses = JSON.parse(await readFile(sourcePath, 'utf8'))
  const contractAddresses = {
    organizerRegistry: rawAddresses.OrganizerRegistry,
    karmaRegistry: rawAddresses.KarmaRegistry,
    electionImplementation: rawAddresses.ElectionImplementation,
    electionFactory: rawAddresses.VESTArElectionFactory,
    mockUsdt: rawAddresses.MockUSDT,
  }

  const moduleSource = formatTsModule(
    [
      `export const vestarDeployment = ${JSON.stringify(rawAddresses, null, 2)} as const;`,
      '',
      `export const vestarDeploymentSource = ${JSON.stringify(filename)} as const;`,
      '',
      `export const vestarContractAddresses = ${JSON.stringify(contractAddresses, null, 2)} as const;`,
      '',
      'export type VestarContractAddressBook = typeof vestarContractAddresses;',
    ].join('\n'),
  )

  await writeFile(targetPath, moduleSource)
}

async function generateIndexModule() {
  // generated 폴더를 외부에서 `./generated` 한 번으로 가져오게 해주는 barrel file
  const targetPath = path.join(outputDir, 'index.ts')
  const exports = [
    'export * from "./addresses";',
    'export * from "./mockUsdtAbi";',
    'export * from "./vestarElectionAbi";',
    'export * from "./vestarElectionFactoryAbi";',
    'export * from "./vestarKarmaRegistryAbi";',
    'export * from "./vestarOrganizerRegistryAbi";',
  ].join('\n')

  await writeFile(targetPath, formatTsModule(exports))
}

async function main() {
  await mkdir(outputDir, { recursive: true })
  await Promise.all([generateAbiModules(), generateAddressesModule(), generateIndexModule()])

  console.log(`Synced VESTAr contract artifacts into ${outputDir}`)
}

await main()
